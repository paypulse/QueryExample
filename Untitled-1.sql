		FROM EVU_STD
		WHERE OPEN_YN ='Y'
		ORDER BY  EVU_YY DESC, EVU_STD_ID DESC

SELECT 
			D.EVU_STD_ID, D.EVU_STD_NM, 
			S.EVU_STEP_CD, S.EVU_STEP_NM, S.EVU_SDATE, S.EVU_EDATE, S.ORDER_NO
		FROM EVU_STEP S INNER JOIN EVU_STD D ON (S.EVU_STD_ID = D.EVU_STD_ID)
		WHERE S.EVU_STEP_CD LIKE '%1' AND D.OPEN_YN = 'Y'
		ORDER BY D.EVU_YY DESC,D.EVU_STD_ID DESC, S.EVU_STEP_CD DESC

 SELECT 
			S.EVU_STD_ID, S.EVU_STD_NM, SP.EVU_STEP_CD, SP.EVU_STEP_NM, 
			ST.EVU_STAT_SEQ, ST.CHASU, ST.EVU_STAT_CD, ST.EVU_STAT_NM
		FROM EVU_STD S INNER JOIN EVU_STEP SP ON (S.EVU_STD_ID = SP.EVU_STD_ID) 
			INNER JOIN EVU_STAT ST ON (SP.EVU_STEP_CD = ST.EVU_STEP_CD)
		WHERE S.EVU_STD_ID = '202201'


